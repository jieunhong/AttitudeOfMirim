package mirim.stu.score;

public class StudentDataBean {
	private int stu_num;
	private String stu_name;
	private int stu_total;
	private int stu_pw;
	
	public int getStu_num() {
		return stu_num;
	}
	public void setStu_num(int stu_num) {
		this.stu_num = stu_num;
	}
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public int getStu_total() {
		return stu_total;
	}
	public void setStu_total(int stu_total) {
		this.stu_total = stu_total;
	}
	public int getStu_pw() {
		return stu_pw;
	}
	public void setStu_pw(int stu_pw) {
		this.stu_pw = stu_pw;
	}
	
	
}

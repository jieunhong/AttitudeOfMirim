package mirim.stu.score;

public class ScoreDataBean {
	private int sc_list;
	private int sc_num;
	private String sc_date;
	private String sc_content;
	private int sc_plus;
	private int sc_minus;
	
	public int getSc_list() {
		return sc_list;
	}
	public void setSc_list(int sc_list) {
		this.sc_list = sc_list;
	}
	public int getSc_num() {
		return sc_num;
	}
	public void setSc_num(int sc_num) {
		this.sc_num = sc_num;
	}
	public String getSc_date() {
		return sc_date;
	}
	public void setSc_date(String sc_date) {
		this.sc_date = sc_date;
	}
	public String getSc_content() {
		return sc_content;
	}
	public void setSc_content(String sc_content) {
		this.sc_content = sc_content;
	}
	public int getSc_plus() {
		return sc_plus;
	}
	public void setSc_plus(int sc_plus) {
		this.sc_plus = sc_plus;
	}
	public int getSc_minus() {
		return sc_minus;
	}
	public void setSc_minus(int sc_minus) {
		this.sc_minus = sc_minus;
	}
	
}
